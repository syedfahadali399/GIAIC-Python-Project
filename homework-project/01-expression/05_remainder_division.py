def remainder_division():
    num_1 = int(input("Enter 1st Number: "))
    num_2 = int(input("Enter 2nd Number: "))

    divide = num_1 / num_2
    remainder = num_1 % num_2
    print(f"The result of {num_1} by {num_2} is divide by {divide} and its remainder is {remainder}")

remainder_division()